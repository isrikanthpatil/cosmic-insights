export const searchPlaces = (query: string): string[] => {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  const results: string[] = [];
  
  // Search in cities first
  const cityMatches = indianCities.filter(city => 
    city.toLowerCase().includes(lowerQuery)
  );
  
  // Search in states
  const stateMatches = indianStates.filter(state => 
    state.toLowerCase().includes(lowerQuery)
  ).map(state => `${state}, India`);
  
  // Combine results, prioritizing exact matches
  const exactMatches = [...cityMatches, ...stateMatches].filter(place =>
    place.toLowerCase().startsWith(lowerQuery)
  );
  
  const partialMatches = [...cityMatches, ...stateMatches].filter(place =>
    !place.toLowerCase().startsWith(lowerQuery) && place.toLowerCase().includes(lowerQuery)
  );
  
  results.push(...exactMatches.slice(0, 5));
  results.push(...partialMatches.slice(0, 5));
  
  // Remove duplicates and limit to 10 results
  return [...new Set(results)].slice(0, 10);
};